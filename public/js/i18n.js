export function t(key, ...args) {
  const dict = i18n[currentLang] || i18n.en;
  const keys = key.split(".");
  let text = dict;

  for (const k of keys) {
    if (text && k in text) {
      text = text[k];
    } else {
      text = null;
      break;
    }
  }

  if (text === null || text === undefined) {
    console.warn(`üåç Missing translation for key: ${key}`);
    return key;
  }

  if (typeof text === "function") {
    return text(...args);
  }

  return text;
}

