// ‰Ω†ÂéüÊúâÁöÑ setLang ÂáΩÊï∏ÔºåÂè™ÈúÄË¶ÅÂ∞è‰øÆÊîπ
export function setLang(langCode = "en") {
  // üîß Êõ¥Êñ∞ÂÖ®ÂüüËÆäÊï∏
  currentLang = langCode;
  
  localStorage.setItem("lang", langCode);
  document.documentElement.lang = langCode;

  const dict = i18n[langCode] || i18n.en;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const keys = key.split(".");
    let text = dict;

    for (const k of keys) {
      if (text && k in text) {
        text = text[k];
      } else {
        text = null;
        break;
      }
    }

    if (typeof text === "string") {
      if (el.tagName === "OPTION") {
        el.textContent = text;
      } else {
        el.innerHTML = text;
      }
    }
  });
  
  // üîß Ê∑ªÂä† placeholder Êõ¥Êñ∞
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    const keys = key.split(".");
    let text = dict;

    for (const k of keys) {
      if (text && k in text) {
        text = text[k];
      } else {
        text = null;
        break;
      }
    }

    if (typeof text === "string") {
      el.placeholder = text;
    }
  });
  
  window.dispatchEvent(new CustomEvent("langChanged", { detail: { lang: langCode } }));
}

